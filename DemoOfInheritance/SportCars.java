package com.DemoOfInheritance;

public class SportCars extends Car {

	private int Horspower;
	private int AirBag;
	
	public int getHorspower() {
		return Horspower;
	}
	public void setHorspower(int horspower) {
		Horspower = horspower;
	}
	public int getAirBag() {
		return AirBag;
	}
	public void setAirBag(int airBag) {
		AirBag = airBag;
	}
	
}
